                                                                                                                                              0,                                                                                                                                                                                                                                                       X(1,1)*Y(2,3)*Z(3,4) - X(1,1)*Y(3,3)*Z(2,4) - X(2,1)*Y(1,3)*Z(3,4) + X(2,1)*Y(3,3)*Z(1,4) + X(3,1)*Y(1,3)*Z(2,4) - X(3,1)*Y(2,3)*Z(1,4),                                                                                                                                                                                                                                                       X(1,1)*X(2,3)*Z(3,4) - X(1,1)*X(3,3)*Z(2,4) + X(1,1)*Y(2,3)*Y(3,4) - X(1,1)*Y(2,4)*Y(3,3) - X(1,3)*X(2,1)*Z(3,4) + X(1,3)*X(3,1)*Z(2,4) + X(2,1)*X(3,3)*Z(1,4) - X(2,1)*Y(1,3)*Y(3,4) + X(2,1)*Y(1,4)*Y(3,3) - X(2,3)*X(3,1)*Z(1,4) + X(3,1)*Y(1,3)*Y(2,4) - X(3,1)*Y(1,4)*Y(2,3),                                                                                                             X(1,1)*X(2,3)*Y(3,4) - X(1,1)*X(2,4)*Y(3,3) - X(1,1)*X(3,3)*Y(2,4) + X(1,1)*X(3,4)*Y(2,3) - X(1,3)*X(2,1)*Y(3,4) + X(1,3)*X(3,1)*Y(2,4) + X(1,4)*X(2,1)*Y(3,3) - X(1,4)*X(3,1)*Y(2,3) + X(2,1)*X(3,3)*Y(1,4) - X(2,1)*X(3,4)*Y(1,3) - X(2,3)*X(3,1)*Y(1,4) + X(2,4)*X(3,1)*Y(1,3), X(1,1)*X(2,3)*X(3,4) - X(1,1)*X(2,4)*X(3,3) - X(1,3)*X(2,1)*X(3,4) + X(1,3)*X(2,4)*X(3,1) + X(1,4)*X(2,1)*X(3,3) - X(1,4)*X(2,3)*X(3,1)
                                                                                                                                              0,                                                                                                                                                                                                                                                       X(1,1)*Y(3,2)*Z(2,4) - X(1,1)*Y(2,2)*Z(3,4) + X(2,1)*Y(1,2)*Z(3,4) - X(2,1)*Y(3,2)*Z(1,4) - X(3,1)*Y(1,2)*Z(2,4) + X(3,1)*Y(2,2)*Z(1,4),                                                                                                                                                                                                                                                       X(1,1)*X(3,2)*Z(2,4) - X(1,1)*X(2,2)*Z(3,4) - X(1,1)*Y(2,2)*Y(3,4) + X(1,1)*Y(2,4)*Y(3,2) + X(1,2)*X(2,1)*Z(3,4) - X(1,2)*X(3,1)*Z(2,4) - X(2,1)*X(3,2)*Z(1,4) + X(2,1)*Y(1,2)*Y(3,4) - X(2,1)*Y(1,4)*Y(3,2) + X(2,2)*X(3,1)*Z(1,4) - X(3,1)*Y(1,2)*Y(2,4) + X(3,1)*Y(1,4)*Y(2,2),                                                                                                             X(1,1)*X(2,4)*Y(3,2) - X(1,1)*X(2,2)*Y(3,4) + X(1,1)*X(3,2)*Y(2,4) - X(1,1)*X(3,4)*Y(2,2) + X(1,2)*X(2,1)*Y(3,4) - X(1,2)*X(3,1)*Y(2,4) - X(1,4)*X(2,1)*Y(3,2) + X(1,4)*X(3,1)*Y(2,2) - X(2,1)*X(3,2)*Y(1,4) + X(2,1)*X(3,4)*Y(1,2) + X(2,2)*X(3,1)*Y(1,4) - X(2,4)*X(3,1)*Y(1,2), X(1,1)*X(2,4)*X(3,2) - X(1,1)*X(2,2)*X(3,4) + X(1,2)*X(2,1)*X(3,4) - X(1,2)*X(2,4)*X(3,1) - X(1,4)*X(2,1)*X(3,2) + X(1,4)*X(2,2)*X(3,1)
