    0, B(3,6)*x1*x2 - B(3,3)*x1*y2 - A(3,4)*x2*z1 + A(3,1)*y2*z1, B(3,8)*x1*x2 - A(3,2)*x1*z2 - B(3,7)*x2*y1 + A(3,1)*y1*z2, A(3,1)*z1 - B(3,7)*x2 - B(3,3)*x1 + A(3,1)*z2 + B(3,9)*x1*x2 - A(3,3)*x1*z2 - A(3,7)*x2*z1];
    
